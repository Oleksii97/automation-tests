// ╔══════════════════════════════════════════════════════════════════════════╗
// ║                    ТЕСТИ ПАГІНАЦІЇ — 4 СТОРІНКИ                        ║
// ║                                                                          ║
// ║  Що таке пагінація?                                                      ║
// ║  Це розбивка великого списку даних на сторінки.                         ║
// ║  Як у книзі — замість показати всі 1000 рядків одразу,                  ║
// ║  показуємо по 3 рядки і даємо кнопки "вперед/назад".                    ║
// ╚══════════════════════════════════════════════════════════════════════════╝

// ─── CONFIG ────────────────────────────────────────────────────────────────
// Це об'єкт з налаштуваннями тесту.
// Виносимо сюди всі "магічні числа" щоб не шукати їх по всьому файлу.
// Якщо сайт зміниться — міняємо тільки тут, а не в кожному тесті.
const CONFIG = {
  url:      'https://practice.expandtesting.com/dynamic-pagination-table',
  pageSize: 3,   // скільки рядків показувати на одній сторінці
                 // вибираємо 3 — тоді отримаємо рівно 4 сторінки (12 записів / 3 = 4)
};

// ─── КОНСТАНТИ ─────────────────────────────────────────────────────────────
// data-dt-idx — це спеціальний атрибут який DataTables ставить на кнопки.
// Замість того щоб писати рядок 'next' скрізь по коду —
// зберігаємо в константу. Якщо атрибут зміниться — міняємо в одному місці.
const NEXT = 'next';
const PREV = 'previous';


// ╔══════════════════════════════════════════════════════════════════════════╗
// ║  describe() — це "папка" для групи тестів.                              ║
// ║  Cypress показує цей текст у звіті як заголовок групи.                  ║
// ╚══════════════════════════════════════════════════════════════════════════╝
describe('Пагінація — 4 сторінки', () => {


  // ── beforeEach ───────────────────────────────────────────────────────────
  // Цей блок запускається ПЕРЕД КОЖНИМ тестом автоматично.
  // Це як "підготовка" — щоб кожен тест починався з чистого стану.
  // Без beforeEach довелось би копіювати ці 3 рядки в кожен it().
  beforeEach(() => {
    // 1. Відкриваємо сторінку
    cy.visit(CONFIG.url);

    // 2. Вибираємо "3" у дропдауні DataTables (select — це HTML <select>)
    //    Це змінює кількість рядків на сторінці з дефолтного на наш CONFIG.pageSize
    cy.get('select[name="example_length"]').select(`${CONFIG.pageSize}`);

    // 3. Чекаємо поки таблиця реально перемалюється.
    //    Cypress не чекає сам — треба явно сказати "чекай поки з'явиться N рядків".
    //    Без цього рядка наступний тест міг би перевіряти ще старі дані.
    cy.get('#example tbody tr').should('have.length', CONFIG.pageSize);
  });


  // ╔════════════════════════════════════════════════════════════════════════╗
  // ║  context() — це "підпапка" всередині describe.                        ║
  // ║  Групуємо тести за темою щоб звіт був зрозумілим.                     ║
  // ╚════════════════════════════════════════════════════════════════════════╝
  context('1. Стартовий стан', () => {

    // it() — це один тест. Назва описує ЩО саме перевіряємо.
    // Правило: назва тесту має читатись як речення — "воно [робить те-то]"

    it('відкривається перша сторінка', () => {
      // cy.assertPage() — це наша кастомна команда з commands.js
      // Вона перевіряє яка сторінка зараз активна в пагінаторі
      cy.assertPage(1);
    });

    it(`на першій сторінці рівно ${CONFIG.pageSize} рядки`, () => {
      // Шаблонний рядок `${CONFIG.pageSize}` підставить "3" в назву тесту
      cy.assertItemsCount(CONFIG.pageSize);
    });

    it('кнопка "Назад" задізейблена на сторінці 1', () => {
      // На першій сторінці нікуди йти назад — кнопка має бути неактивна.
      // Передаємо константу PREV = 'previous' — це значення data-dt-idx кнопки
      cy.assertDisabled(PREV);
    });

    it('кнопка "Вперед" активна на сторінці 1', () => {
      // На першій сторінці є куди йти вперед — кнопка має бути активна
      cy.assertEnabled(NEXT);
    });

  });


  context('2. Послідовний прохід вперед (1 → 4)', () => {

    it('перехід 1 → 2', () => {
      // cy.clickPage() — наша кастомна команда.
      // Вона спочатку перевіряє що кнопка не disabled, потім клікає.
      // Звичайний .click() без перевірки міг би клікнути на заблоковану кнопку.
      cy.clickPage(NEXT);

      // Після кліку перевіряємо два факти:
      cy.assertPage(2);                    // 1. активна сторінка змінилась на 2
      cy.assertItemsCount(CONFIG.pageSize); // 2. кількість рядків правильна
    });

    it('перехід 1 → 2 → 3', () => {
      cy.clickPage(NEXT); // сторінка 1 → 2
      cy.clickPage(NEXT); // сторінка 2 → 3
      cy.assertPage(3);
      cy.assertItemsCount(CONFIG.pageSize);
    });

    it('перехід 1 → 2 → 3 → 4', () => {
      cy.clickPage(NEXT); // → 2
      cy.clickPage(NEXT); // → 3
      cy.clickPage(NEXT); // → 4 (остання)
      cy.assertPage(4);
      // На останній сторінці може бути менше рядків — тому не перевіряємо count
    });

    it('кнопка "Вперед" задізейблена на сторінці 4', () => {
      // Переходимо на сторінку 4 прямим кліком на цифру "4" в пагінаторі
      cy.clickPageNumber(4);
      // На останній сторінці нікуди йти вперед
      cy.assertDisabled(NEXT);
    });

    it('кнопка "Назад" активна на сторінці 4', () => {
      cy.clickPageNumber(4);
      // На останній сторінці є куди йти назад
      cy.assertEnabled(PREV);
    });

  });


  context('3. Послідовний прохід назад (4 → 1)', () => {

    // Цей beforeEach спрацює ДОДАТКОВО до верхнього beforeEach.
    // Тобто порядок буде: visit → select → wait → clickPageNumber(4)
    // Кожен тест у цьому context стартує з сторінки 4
    beforeEach(() => {
      cy.clickPageNumber(4);
      cy.assertPage(4); // переконуємось що справді перейшли
    });

    it('перехід 4 → 3', () => {
      cy.clickPage(PREV);
      cy.assertPage(3);
    });

    it('перехід 4 → 3 → 2', () => {
      cy.clickPage(PREV); // → 3
      cy.clickPage(PREV); // → 2
      cy.assertPage(2);
    });

    it('перехід 4 → 3 → 2 → 1', () => {
      cy.clickPage(PREV); // → 3
      cy.clickPage(PREV); // → 2
      cy.clickPage(PREV); // → 1
      cy.assertPage(1);
      // Додатково перевіряємо стан кнопки — на першій сторінці PREV має бути disabled
      cy.assertDisabled(PREV);
    });

  });


  context('4. Прямий перехід (кліки на номер сторінки)', () => {

    // forEach — це JavaScript метод масиву.
    // Замість писати 4 однакових тести вручну — генеруємо їх у циклі.
    // [1, 2, 3, 4].forEach → створить 4 тести автоматично
    [1, 2, 3, 4].forEach((pageNum) => {
      it(`клік на сторінку ${pageNum}`, () => {
        cy.clickPageNumber(pageNum); // клік на цифру в пагінаторі
        cy.assertPage(pageNum);      // перевіряємо що та сторінка стала активною
      });
    });

  });


  context('5. Крайні сторінки', () => {

    it('сторінка 4 — Next disabled, Prev enabled', () => {
      cy.clickPageNumber(4);
      cy.assertDisabled(NEXT);  // нікуди йти вперед
      cy.assertEnabled(PREV);   // є куди йти назад
    });

    it('сторінка 1 — Prev disabled, Next enabled', () => {
      cy.clickPageNumber(4);    // спочатку йдемо на 4
      cy.clickPageNumber(1);    // потім повертаємось на 1
      cy.assertDisabled(PREV);  // нікуди йти назад
      cy.assertEnabled(NEXT);   // є куди йти вперед
    });

    it('з сторінки 2 перейти одразу на 4', () => {
      cy.clickPageNumber(2);
      cy.assertPage(2);
      cy.clickPageNumber(4);
      cy.assertPage(4);
    });

  });


  context('6. Цілісність даних', () => {

    it('немає дублювання рядків між сторінками', () => {
      // Збираємо імена з усіх 4 сторінок в масив
      const allNames = [];

      // Cypress._.times(4) — це як for(i=0; i<4; i++) але в стилі Cypress
      Cypress._.times(4, (i) => {
        // На першій сторінці (i=0) вже знаходимось — клікати не треба
        if (i > 0) cy.clickPage(NEXT);

        // Збираємо текст першої колонки кожного рядка
        cy.get('#example tbody tr td:first-child').each(($td) => {
          allNames.push($td.text().trim());
        });
      });

      cy.then(() => {
        // Set автоматично видаляє дублі.
        // Якщо розмір Set == розмір масиву — дублів немає
        const unique = [...new Set(allNames)];
        expect(unique.length).to.eq(allNames.length);
      });
    });

    it('загальна кількість рядків збігається з "Showing X to Y of Z"', () => {
      let total = 0;

      Cypress._.times(4, (i) => {
        if (i > 0) cy.clickPage(NEXT);
        // Додаємо кількість рядків на поточній сторінці до загального лічильника
        cy.get('#example tbody tr').then(($rows) => { total += $rows.length; });
      });

      cy.then(() => {
        // DataTables показує рядок "Showing 1 to 3 of 12 entries"
        // Витягуємо число після "of" і порівнюємо з нашим total
        cy.get('#example_info').invoke('text').then((text) => {
          const match = text.match(/of (\d+)/); // регулярний вираз: знайди "of 12"
          if (match) expect(total).to.eq(Number(match[1]));
        });
      });
    });

  });

});
