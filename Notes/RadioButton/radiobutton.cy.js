// ╔══════════════════════════════════════════════════════════════════════════╗
// ║                     ТЕСТИ РАДІОКНОПОК (Radio Buttons)                  ║
// ║  Сайт: https://practice.expandtesting.com/radio-buttons                 ║
// ║                                                                          ║
// ║  Реальна структура сайту:                                                ║
// ║  Кнопки: #blue, #red, #yellow, #black — активні                         ║
// ║  Кнопка: #green — disabled                                               ║
// ║  За замовчуванням: #blue вибрана                                         ║
// ║  data-testid="result" — НЕ існує на цьому сайті                          ║
// ╚══════════════════════════════════════════════════════════════════════════╝

describe('Radio Button — повний набір тестів', () => {

  beforeEach(() => {
    cy.visit('https://practice.expandtesting.com/radio-buttons');
  });


  // ╔════════════════════════════════════════════════════════════════════════╗
  // ║  1. СТАРТОВИЙ СТАН                                                    ║
  // ╚════════════════════════════════════════════════════════════════════════╝
  context('1. Стартовий стан', () => {

    it('радіокнопки існують на сторінці', () => {
      cy.get('[type="radio"]').should('exist');
    });

    it('за замовчуванням вибрана тільки одна кнопка (blue)', () => {
      // На цьому сайті #blue вибрана за замовчуванням — це нормальна поведінка
      cy.get('#blue').should('be.checked');

      // Всі інші активні — не вибрані
      cy.get('[type="radio"]').not('#blue').not('[disabled]').each(($radio) => {
        cy.wrap($radio).should('not.be.checked');
      });
    });

    it('всього вибрана рівно одна кнопка за замовчуванням', () => {
      cy.get('[type="radio"]:checked').should('have.length', 2); // На цьому сайті є дві групи радіокнопок, тому може бути вибрано дві кнопки одночасно
    });

    it('всі активні радіокнопки видимі і доступні', () => {
      cy.get('[type="radio"]').not('[disabled]').each(($radio) => {
        cy.wrap($radio)
          .should('be.visible')
          .and('be.enabled');
      });
    });

  });


  // ╔════════════════════════════════════════════════════════════════════════╗
  // ║  2. ВИБІР РАДІОКНОПКИ                                                 ║
  // ╚════════════════════════════════════════════════════════════════════════╝
  context('2. Вибір радіокнопки', () => {

    it('можна вибрати радіокнопку через .check()', () => {
      cy.get('#red').check().should('be.checked');
    });

    it('вибір через клік на label теж працює', () => {
      cy.get('label[for="yellow"]').click();
      cy.get('#yellow').should('be.checked');
    });

    it('можна вибрати кнопку по value атрибуту', () => {
      cy.get('[type="radio"]').check('black');
      cy.get('[type="radio"][value="black"]').should('be.checked');
    });

    it('можна вибрати кнопку по id', () => {
      cy.get('#yellow').check().should('be.checked');
    });

  });


  // ╔════════════════════════════════════════════════════════════════════════╗
  // ║  3. ЕКСКЛЮЗИВНИЙ ВИБІР                                                ║
  // ╚════════════════════════════════════════════════════════════════════════╝
  context('3. Тільки одна кнопка може бути вибрана', () => {

    it('при виборі red — blue знімається', () => {
      cy.get('#blue').should('be.checked'); // blue вибрана за замовчуванням

      cy.get('#red').check();

      cy.get('#red').should('be.checked');
      cy.get('#blue').should('not.be.checked');
    });

    it('при виборі yellow — red і blue знімаються', () => {
      cy.get('#red').check();
      cy.get('#yellow').check();

      cy.get('#yellow').should('be.checked');
      cy.get('#red').should('not.be.checked');
      cy.get('#blue').should('not.be.checked');
    });

    it('в будь-який момент вибрана рівно одна кнопка', () => {
      cy.get('#black').check();
      cy.get('[type="radio"]:checked').should('have.length', 2); // На цьому сайті є дві групи радіокнопок, тому може бути вибрано дві кнопки одночасно
    });

  });


  // ╔════════════════════════════════════════════════════════════════════════╗
  // ║  4. ЗАДІЗЕЙБЛЕНА КНОПКА                                               ║
  // ╚════════════════════════════════════════════════════════════════════════╝
  context('4. Задізейблена радіокнопка (#green)', () => {

    it('задізейблена кнопка існує на сторінці', () => {
      cy.get('#green').should('exist');
    });

    it('задізейблена кнопка має атрибут disabled', () => {
      cy.get('#green')
        .should('be.disabled')
        .and('have.attr', 'disabled');
    });

  });


  // ╔════════════════════════════════════════════════════════════════════════╗
  // ║  5. РЕЗУЛЬТАТ ПІСЛЯ ВИБОРУ                                            ║
  // ║  На цьому сайті немає текстового результату —                         ║
  // ║  кнопка просто змінює стан на checked                                 ║
  // ╚════════════════════════════════════════════════════════════════════════╝
  context('5. Стан після вибору', () => {

    it('після вибору кнопка має атрибут checked', () => {
      cy.get('#yellow').check();
      cy.get('#yellow').should('be.checked');
    });

    it('стан змінюється при виборі іншої кнопки', () => {
      cy.get('#red').check();
      cy.get('#red').should('be.checked');

      cy.get('#yellow').check();
      cy.get('#yellow').should('be.checked');
      cy.get('#red').should('not.be.checked');
    });

  });


  // ╔════════════════════════════════════════════════════════════════════════╗
  // ║  6. ПАРАМЕТРИЗОВАНІ ТЕСТИ                                             ║
  // ╚════════════════════════════════════════════════════════════════════════╝
  context('6. Перебір всіх активних кнопок', () => {

    it('кожна активна кнопка може бути вибрана', () => {
      cy.get('[type="radio"]').not('[disabled]').each(($radio, index) => {
        cy.wrap($radio).check().should('be.checked');

        // Після вибору — вибрана рівно одна кнопка
        cy.get('[type="radio"]:checked').should('have.length', 2); // На цьому сайті є дві групи радіокнопок, тому може бути вибрано дві кнопки одночасно 

        cy.log(` Радіокнопка #${index + 1} успішно вибрана`);
      });
    });

  });

});